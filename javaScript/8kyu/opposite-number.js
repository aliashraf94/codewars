// Very simple, given an integer or a floating-point number, find its opposite.

// Examples:

// 1: -1
// 14: -14
// -34: 34

// My Soultion

function opposite(number) {
    return -number
}

// Other solution

const opposite = number => -number;

// Other solution

function opposite(number) {
    return number * (-1);
}

// Other solution

const opposite = n => -n;